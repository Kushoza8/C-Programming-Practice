#include <stdio.h>
int main(){
    char c;
    printf("enter a char: ");
    scanf("%c",&c);
    printf("the character is %c\n",c);
    int a;
    printf("enter an integer: ");
    scanf("%d",&a);
    printf("the integer is %d\n",a);
    float f;
    printf("enter a float: ");
    scanf("%f",&f);
    printf("the float is %f\n",f);
    return 0;
}